const keyFor = (a, b) => [a, b].sort().join("+");

const archetypeByDimensionPair = new Map(
  archetypes.map((archetype) => [keyFor(archetype.core[0], archetype.core[1]), archetype]),
);

export function getArchetypeForDimensions(first, second) {
  const archetype = archetypeByDimensionPair.get(keyFor(first, second));
  if (!archetype) {
    throw new Error(`No Career DNA archetype for ${first} + ${second}`);
  }
  return archetype;
}

export function getTopDimensions(scores) {
  return Object.entries(scores)
    .sort(([, a], [, b]) => b - a)
    .slice(0, 2)
    .map(([dimension]) => dimension);
}

export function getArchetypeForScores(scores) {
  const [first, second] = getTopDimensions(scores);
  return getArchetypeForDimensions(first, second);
}
